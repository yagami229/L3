print('Введите радиус круга (в сантиметрах):')
r = float(input())
pi = 3.14
print('Длина окружности =', round(2*pi*r,1), 'см,',(2*pi*r)/100, 'м')
print('Площадь круга =', round(pi*r**2,1), 'см^2', (pi*r**2)/10000, 'м^2')
print('Сторона вписанного квадрата =', round(2*r/2**(1/2),1), 'см', round((2*r/2**(1/2))/100,3), 'м' )
print('Сторона вписанного равностороннего треугольника =', round(2*3**(1/2)*r,1), 'см', round((2*3**(1/2)*r)/100,3), 'м' )
print('Сторона описанного квадрата =', round(2*r*2**0.5,1), 'см', round((2*r*2**0.5)/100,3), 'м')
print('Сторона описанного равностороннего треугольника =', round(r*3**0.5,1), 'см', round((r*3**0.5)/100,3), 'м')
print('Сторона описанного правильного восьмиугольника = ', round(2*r*(2**0.5 - 1),1), 'см', round((2*r*(2**0.5 - 1)/100),3), 'м')
